# Программа, которая требует ввести 2 целых числа, строку и 1 дробное число
# далее выводит на экран строку и сумму двух целых и дробного числа
num1 = int(input("Введите первое целое число: "))
num2 = int(input("Введите второе целое число: "))
name = input("Введите строку")
f_num = float(input("Введите дробное число: "))
print(name)
print(num1 + num2 + f_num)
